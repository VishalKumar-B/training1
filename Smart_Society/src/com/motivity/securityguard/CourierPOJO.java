package com.motivity.securityguard;

public class CourierPOJO {

	private int courier_id;
	private String courier_receiver_name;
	private int courier_flat_number;
	private String courier_from_address;
	private String courier_received_date;
	private String courier_received_time;

	public int getCourier_id() {
		return courier_id;
	}

	public void setCourier_id(int courier_id) {
		this.courier_id = courier_id;
	}

	public String getCourier_receiver_name() {
		return courier_receiver_name;
	}

	public void setCourier_receiver_name(String courier_receiver_name) {
		this.courier_receiver_name = courier_receiver_name;
	}

	public int getCourier_flat_number() {
		return courier_flat_number;
	}

	public void setCourier_flat_number(int courier_flat_number) {
		this.courier_flat_number = courier_flat_number;
	}

	public String getCourier_from_address() {
		return courier_from_address;
	}

	public void setCourier_from_address(String courier_from_address) {
		this.courier_from_address = courier_from_address;
	}

	public String getCourier_received_time() {
		return courier_received_time;
	}

	public void setCourier_received_time(String courier_received_time) {
		this.courier_received_time = courier_received_time;
	}

	public String getCourier_received_date() {
		return courier_received_date;
	}

	public void setCourier_received_date(String courier_received_date) {
		this.courier_received_date = courier_received_date;
	}
	
	
}
